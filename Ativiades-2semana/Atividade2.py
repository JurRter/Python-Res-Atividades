##O loop While e o Break
#Crie um programa que use um loop while True: para pedir continuamente que o usuário digite um número inteiro.

#Se o usuário digitar um número positivo ou zero, o programa deve apenas continuar pedindo o próximo número.
#Se o usuário digitar um número negativo (menor que 0), o programa deve usar o comando break para encerrar o loop e imprimir a mensagem "Programa encerrado!".